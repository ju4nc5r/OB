class Calc:

    def suma(self, a, b):
        suma = a + b
        print('El valor de la suma es: ' + str(suma))

    def resta(self, a, b):
        resta = a - b
        print('El valor de la resta es: ' + str(resta))

    def multiplicacion(self, a, b):
        mult = a * b
        print('El valor de la multiplicacion es: ' + str(mult))

    def division(self, a, b):
        div = a / b
        print('El valor de la division es: ' + str(div))


